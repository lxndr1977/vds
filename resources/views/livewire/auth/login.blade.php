                        {{ __('Forgot your password?') }}
                    </flux:link>
                @endif
            </div>

            <!-- Remember Me -->
            <x-checkbox label="{{ __('Remember me') }}" wire:model="remember" />

            <div class="flex items-center justify-end">
                <x-mary-button type="submit" class="w-full btn-primary">{{ __('Log in') }}</x-mary-button>
            </div>
        </form>
